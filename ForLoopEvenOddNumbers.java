public class ForLoopEvenOddNumbers {
    public static void main(String[] args) {
        for(int a=2; a<=25;a+=2) {
            System.out.println(" An even number =" + a);
        }
        for (int b=1; b<=25;b+=2) {
            System.out.println( " An odd number=" + b);

        }
        }
    }

